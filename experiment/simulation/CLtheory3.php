<h4> Definition</h4></br>

<b>Tokens</b> correspond to the total number of word counts in a text while <b>type</b> corresponds to the total count of unique words in a text. We can say that language consists of various types of words and all the particular instances of these words are called tokens.
<br/><br/>
For Eg: Do not waste time as wasting time does a lot of harm.
<br/><br/> Here, <br/>
#tokens=12 <br/>
#types=11 (time has been repeated twice) 
<br/><br/>
<h4>Type vs token distinction</h4><br/>

The type/token distinction is related to that between universals and particulars. Tokens are concrete particular instances of a general and abstract type.<br/>
There is only one word 'the' (type) but many instances of it found on this page (token). <br/><br/>

The type/token distinction is applicable beyond language as well. <br/>
